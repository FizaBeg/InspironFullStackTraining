package homeWork;

public class decrementOp {
	public static void main(String[] args) {
		int i=100;
		
		System.out.println(i); //print 100
		System.out.println(i--);  //print 100 and then decrement  
		System.out.println(i);    //print 99 as decremented
		System.out.println();
		
		int s= 20;
		
		System.out.println(s);    //print 20
		System.out.println(--s);  //decremnet first then print 20
		System.out.println(s);    //print 20
		
	}
}
