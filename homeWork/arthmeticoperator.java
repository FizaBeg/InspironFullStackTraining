package homeWork;

public class arthmeticoperator {
	public static void main(String[] args) {
		
		int i=100;
		i-=10;   // i=i-10
		System.out.println(i);  //print 90
		
		int j=50;
		j/=5;    //j=j/5
		System.out.println(j);  //print 10
		
		int k=15;
		k%=2;    //k=k%2
		System.out.println(k);  //print 1
	}

}
